
//tipo de varliable Let != var & Const
let producto = "Tablet";
console.log(producto);
//permite reasignar el valor a la variable
producto = "Monitor de 24 Pulgadas"
console.log(producto);
//tambíen es de tipo dinamico, por lo que no se requiere definir el tipo de dato pero si lo detecta de forma automatica
producto = 20;
console.log(producto);
// recibe cualquier tipo de dato
producto = true;
console.log(producto);
producto = false;
console.log(producto);
producto = null;
console.log(producto);


//con let también se pueden crear variables sin asignarles valor al momento de su defición y asignar posteriormente.
let precio;
console.log(precio);
precio = 300;
console.log(precio);